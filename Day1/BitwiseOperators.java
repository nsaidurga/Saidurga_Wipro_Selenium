//write a program on Bitwise Operators.
package Day1;

public class BitwiseOperators {

	public static void main(String[] args) {
		int a = 5, b = 3;
        System.out.println((a & b));  
        System.out.println((a | b));  
        System.out.println((a ^ b));  
        System.out.println((~a));        
        System.out.println((a << 1));
        System.out.println((a >> 1));
        }

}
